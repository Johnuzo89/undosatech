-- Auto-clean logs older than 30 days (keep recent studies fast)
CREATE OR REPLACE FUNCTION delete_old_study_logs() RETURNS trigger AS $$
BEGIN
    DELETE FROM study_logs
    WHERE logged_at < NOW() - INTERVAL '30 days';
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER cleanup_study_logs
    AFTER INSERT ON study_logs
    FOR EACH ROW EXECUTE FUNCTION delete_old_study_logs();

-- ── Indexes ───────────────────────────────────────────────────────────────────

CREATE INDEX IF NOT EXISTS idx_studies_user_id     ON studies(user_id);
CREATE INDEX IF NOT EXISTS idx_studies_status      ON studies(status);
CREATE INDEX IF NOT EXISTS idx_studies_created_at  ON studies(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_study_logs_study_id ON study_logs(study_id);
CREATE INDEX IF NOT EXISTS idx_study_logs_logged_at ON study_logs(logged_at DESC);
CREATE INDEX IF NOT EXISTS idx_study_rounds_study  ON study_rounds(study_id, round_number);

-- ── Row Level Security ────────────────────────────────────────────────────────

ALTER TABLE studies      ENABLE ROW LEVEL SECURITY;
ALTER TABLE study_logs   ENABLE ROW LEVEL SECURITY;
ALTER TABLE study_rounds ENABLE ROW LEVEL SECURITY;

-- Users can only see/modify their own studies
CREATE POLICY "users_own_studies"
    ON studies FOR ALL
    TO authenticated
    USING (user_id = auth.uid())
    WITH CHECK (user_id = auth.uid());

CREATE POLICY "users_own_study_logs"
    ON study_logs FOR SELECT
    TO authenticated
    USING (
        study_id IN (
            SELECT id FROM studies WHERE user_id = auth.uid()
        )
    );

CREATE POLICY "users_own_study_rounds"
    ON study_rounds FOR SELECT
    TO authenticated
    USING (
        study_id IN (
            SELECT id FROM studies WHERE user_id = auth.uid()
        )
    );

-- Service role (FastAPI backend) can do everything
CREATE POLICY "service_role_studies"
    ON studies FOR ALL TO service_role
    USING (true) WITH CHECK (true);

CREATE POLICY "service_role_study_logs"
    ON study_logs FOR ALL TO service_role
    USING (true) WITH CHECK (true);

CREATE POLICY "service_role_study_rounds"
    ON study_rounds FOR ALL TO service_role
    USING (true) WITH CHECK (true);

-- ── Useful views ──────────────────────────────────────────────────────────────

CREATE OR REPLACE VIEW studies_summary AS
SELECT
    s.id,
    s.user_id,
    s.user_email,
    s.name,
    s.model,
    s.dataset,
    s.status,
    s.current_round,
    s.total_rounds,
    s.progress_pct,
    s.final_accuracy,
    s.dp_enabled,
    s.nodes,
    s.created_at,
    s.started_at,
    s.completed_at,
    EXTRACT(EPOCH FROM (COALESCE(s.completed_at, NOW()) - s.started_at))::INTEGER AS duration_seconds,
    (SELECT COUNT(*) FROM study_logs l WHERE l.study_id = s.id) AS log_count
FROM studies s;

COMMENT ON TABLE studies IS 'Federated learning study configurations and results. One row per study, scoped per user.';
COMMENT ON TABLE study_logs IS 'Training log lines per study. High-write; auto-cleaned after 30 days.';
COMMENT ON TABLE study_rounds IS 'Per-round aggregated metrics for charting training progress.';
