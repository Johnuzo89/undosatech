-- ── Studies ──────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS studies (
    id TEXT PRIMARY KEY,                        -- UUID generated by FastAPI
    
    -- Ownership
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    user_email TEXT,                            -- denormalised for admin queries

    -- Configuration (immutable after launch)
    name TEXT NOT NULL,
    model TEXT NOT NULL,                        -- ResNet-18, EfficientNet-B0, etc.
    dataset TEXT NOT NULL,                      -- PathMNIST, ChestMNIST, etc.
    num_rounds INTEGER NOT NULL DEFAULT 5,
    nodes TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[],   -- node_ids participating

    -- Differential privacy (null = disabled, ready for Priority 2)
    dp_enabled BOOLEAN DEFAULT FALSE,
    dp_epsilon NUMERIC,
    dp_delta NUMERIC,
    dp_noise_multiplier NUMERIC,

    -- Status lifecycle
    -- queued → running → completed | failed | stopped
    status TEXT NOT NULL DEFAULT 'queued'
        CHECK (status IN ('queued', 'running', 'completed', 'failed', 'stopped')),
    
    -- Progress
    current_round INTEGER DEFAULT 0,
    total_rounds INTEGER,
    progress_pct NUMERIC GENERATED ALWAYS AS (
        CASE WHEN total_rounds > 0
             THEN ROUND((current_round::NUMERIC / total_rounds) * 100, 1)
             ELSE 0 END
    ) STORED,

    -- Results (written on completion)
    final_accuracy NUMERIC,
    final_loss NUMERIC,
    per_class_accuracy JSONB,                  -- { "class_0": 0.91, ... }
    model_download_path TEXT,                  -- Railway path to .pth file

    -- Failure info
    error_message TEXT,

    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT NOW(),
    started_at TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- ── Training logs ─────────────────────────────────────────────────────────────
-- Separate table: logs are written every few seconds during training.
-- Keeping them out of studies avoids row bloat and makes polling fast.

CREATE TABLE IF NOT EXISTS study_logs (
    id BIGSERIAL PRIMARY KEY,
    study_id TEXT NOT NULL REFERENCES studies(id) ON DELETE CASCADE,
    round_number INTEGER,
    logged_at TIMESTAMPTZ DEFAULT NOW(),
    level TEXT DEFAULT 'info'                  -- info | warning | error
        CHECK (level IN ('info', 'warning', 'error')),
    message TEXT NOT NULL,
    metrics JSONB                              -- { accuracy, loss, node_id, ... }
);

-- ── Round metrics (one row per FL round, for charting) ────────────────────────

CREATE TABLE IF NOT EXISTS study_rounds (
    id BIGSERIAL PRIMARY KEY,
    study_id TEXT NOT NULL REFERENCES studies(id) ON DELETE CASCADE,
    round_number INTEGER NOT NULL,
    recorded_at TIMESTAMPTZ DEFAULT NOW(),
    
    -- Aggregated metrics across all nodes
    accuracy NUMERIC,
    loss NUMERIC,
    val_accuracy NUMERIC,
    val_loss NUMERIC,
    
    -- Per-node breakdown
    node_metrics JSONB,                        -- { "node_id": { accuracy, loss, samples } }
    
    UNIQUE (study_id, round_number)
);

-- ── Triggers ──────────────────────────────────────────────────────────────────

CREATE OR REPLACE TRIGGER studies_updated_at
    BEFORE UPDATE ON studies
    FOR EACH ROW EXECUTE FUNCTION update_updated_at();
