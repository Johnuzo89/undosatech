-- ── Medical Imaging extension (MI-CDM) ────────────────────────
-- OHDSI Medical Imaging workgroup extension: links DICOM studies
-- and derived image features to the clinical event tables.
create table if not exists omop.image_occurrence (
    image_occurrence_id     bigint primary key generated by default as identity,
    person_id               bigint  not null references omop.person(person_id),
    procedure_occurrence_id bigint references omop.procedure_occurrence(procedure_occurrence_id),
    visit_occurrence_id     bigint references omop.visit_occurrence(visit_occurrence_id),
    anatomic_site_concept_id integer,
    wadors_uri              text,
    local_path              text,
    image_occurrence_date   date not null,
    image_study_uid         varchar(250) not null,
    image_series_uid        varchar(250),
    modality_concept_id     integer,
    modality_source_value   varchar(50)   -- e.g. OPT (OCT), OP (fundus), MR, CT
);

create table if not exists omop.image_feature (
    image_feature_id             bigint primary key generated by default as identity,
    person_id                    bigint  not null references omop.person(person_id),
    image_occurrence_id          bigint  not null references omop.image_occurrence(image_occurrence_id),
    image_feature_event_id       bigint,
    image_feature_event_field_concept_id integer,
    image_feature_concept_id     integer not null,
    image_feature_type_concept_id integer not null,
    image_finding_concept_id     integer,
    image_finding_num            numeric,
    anatomic_site_concept_id     integer,
    alg_system                   varchar(255),   -- algorithm / model that produced the feature
    alg_datetime                 timestamp
