-- ── Security: RLS on all OMOP tables (service role writes) ────
do $$
declare t record;
begin
    for t in select tablename from pg_tables where schemaname = 'omop' loop
        execute format('alter table omop.%I enable row level security', t.tablename);
        execute format('drop policy if exists "omop read for authenticated" on omop.%I', t.tablename);
        execute format(
            'create policy "omop read for authenticated" on omop.%I for select to authenticated using (true)',
            t.tablename
        );
    end loop;
end $$;

grant usage on schema omop to authenticated, service_role;
grant select on all tables in schema omop to authenticated;
grant all on all tables in schema omop to service_role;
alter default privileges in schema omop grant select on tables to authenticated;
